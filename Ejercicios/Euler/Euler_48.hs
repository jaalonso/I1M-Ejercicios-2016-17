-- Calcular los últimos 10 dígitos de la serie
--    1^1 + 2^2 + 3^3 + ... + 1000^1000
-- ---------------------------------------------------------------------

-- 1ª solución: enrnarbej
-- ======================

euler48 :: Integer
euler48 = sum [x^x `mod` 10^10 | x <- [1..1000]] `mod` 10^10

